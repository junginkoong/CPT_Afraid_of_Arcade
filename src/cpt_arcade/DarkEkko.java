    public static void movementRight() {
        if (level[UserLocationY][UserLocationX + 1] == 4) {
            KeyFragCount += 1;
            MonsterCounter = true;
            MonsterX = UserLocationX + 1;
            MonsterY = UserLocationY;
            gamePoint += 20;
        } else if (level[UserLocationY][UserLocationX + 1] == 6) {
            ItemChoice = RandItem();
        }
        if (level[UserLocationY][UserLocationX + 1] != 1) {
            level[UserLocationY][UserLocationX] = 0;
            UserLocationX += 1;
            level[UserLocationY][UserLocationX] = 3;
        }
    }

    public static void movementLeft() {
        if (level[UserLocationY][UserLocationX - 1] == 4) {
            KeyFragCount += 1;
            MonsterCounter = true;
            MonsterX = UserLocationX - 1;
            MonsterY = UserLocationY;
            gamePoint += 20;
        } else if (level[UserLocationY][UserLocationX - 1] == 6) {
            ItemChoice = RandItem();
        }
        if (level[UserLocationY][UserLocationX - 1] != 1) {
            level[UserLocationY][UserLocationX] = 0;
            UserLocationX -= 1;
            level[UserLocationY][UserLocationX] = 3;
        }
    }

    public static void movementUp() {
        if (level[UserLocationY - 1][UserLocationX] == 4) {
            KeyFragCount += 1;
            MonsterCounter = true;
            MonsterX = UserLocationX;
            MonsterY = UserLocationY - 1;
            gamePoint += 20;
        } else if (level[UserLocationY - 1][UserLocationX] == 6) {
            ItemChoice = RandItem();
        }
        if (level[UserLocationY - 1][UserLocationX] != 1) {
            level[UserLocationY][UserLocationX] = 0;
            UserLocationY -= 1;
            level[UserLocationY][UserLocationX] = 3;
        }
    }

    public static void movementDown() {
        if (level[UserLocationY + 1][UserLocationX] == 4) {
            KeyFragCount += 1;
            MonsterCounter = true;
            MonsterX = UserLocationX;
            MonsterY = UserLocationY + 1;
        } else if (level[UserLocationY + 1][UserLocationX] == 6) {
            ItemChoice = RandItem();
        }
        if (KeyFragCount == 3 && level[UserLocationY + 1][UserLocationX] == 5) {
            gameOver = true;
            gamePoint += 50;
        }
        if (level[UserLocationY + 1][UserLocationX] != 1 && level[UserLocationY + 1][UserLocationX] != 5) {
            level[UserLocationY][UserLocationX] = 0;
            UserLocationY += 1;
            level[UserLocationY][UserLocationX] = 3;
        }
    }

    public static void collisionDetection() {
        int CenterMonsterX, CenterMonsterY, UserX = (UserLocationX * 10 - (sizeMonster / 2)), UserY = (UserLocationY * 10 - (sizeMonster / 2)), height = (10 + sizeMonster), length = (10 + sizeMonster);
        for (int i = 0; i < MonsterLocation.size(); i += 2) {
            CenterMonsterX = MonsterLocation.get(i) + (sizeMonster / 2);
            CenterMonsterY = MonsterLocation.get(i + 1) + (sizeMonster / 2);
            if (CenterMonsterX > UserX && CenterMonsterX < (UserX + length) && CenterMonsterY > UserY && CenterMonsterY < (UserY + height)) {
                gameOver = true;
            }
        }
    }

